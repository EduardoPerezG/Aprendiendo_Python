# Se asigna valor a una variable
nombre = "Eduardo"
print(nombre)

# Se reasigna valor a la misma variable anterior
nombre = "Luis"
print(nombre)

# Se imprime directo la suma de valores de dos variables
edad = 23
print(edad)
edad2 = 20
print(edad2)
print(edad + edad2)

# Se concatena el valor de dos variables distintas en una tercer variable
palabra1 = "Hola"
palabra2 = " Python"
frase = palabra1 + palabra2
print(frase)

# Se hace la suma de dos variables pero se guarda en una tercer variable
num1 = 30
num2 = 50
suma = num1 + num2
print(suma)

